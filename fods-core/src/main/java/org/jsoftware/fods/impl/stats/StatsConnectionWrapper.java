	public StatsConnectionWrapper(StatisticsItem statisticsItem, Connection conn, int logLongSQLs, Logger logger, String dbName) {
		this.statItem = statisticsItem;
		this.connection = conn;
		this.statItem.get++;
		this.logSQL = logLongSQLs;
		this.logger = logger;
		this.dbName = dbName;
	}



